let undefinedVar1: undefined; 
let undefinedVar2: undefined; 
let numVar: number; 
let noTypeVar; 
numVar = 10;
console.log("undefinedVar1:", undefinedVar1);
console.log("undefinedVar2:", undefinedVar2);
console.log("numVar:", numVar);
console.log("noTypeVar:", noTypeVar);
//undefinedVar1 và undefinedVar2: Đây là hai biến được khai báo với kiểu dữ liệu undefined. Vì không có giá trị được gán cho chúng, giá trị mặc định của chúng sẽ là undefined.
//noTypeVar: Đây là biến không có kiểu dữ liệu được đặt. TypeScript sẽ gán giá trị mặc định undefined cho biến này khi không có giá trị được gán cho nó.
